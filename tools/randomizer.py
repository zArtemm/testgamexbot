import random


def get_random_emoji():
    emojies = ["🤭", "😋", "😉", "🙃", "😲"]
    return random.choice(emojies)


def random_money_emoji():
    emojies = ["💵", "💰", "🤑", "💶", "💸", "💷"]
    return random.choice(emojies)


def random_casino_emoji():
    emojies = ["🎰", "🍒", "♦️", "⭐", "🚀"]
    return random.choice(emojies)
